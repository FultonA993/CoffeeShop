    private void populateCoffeeTable() {
        Connection conn = null;
        Statement stmt = null;
        ResultSet rs = null;

        try {
            Class.forName("org.apache.derby.jdbc.EmbeddedDriver");

            conn = DriverManager.getConnection(URL);
            stmt = conn.createStatement();

            String query = "SELECT * FROM Coffee";
            rs = stmt.executeQuery(query);
            DefaultTableModel dtm = new DefaultTableModel();
            dtm.addColumn("Coffee ID");
            dtm.addColumn("Coffee Name");
            dtm.addColumn("Description");
            dtm.addColumn("Price");
            dtm.addColumn("Stock");

            // Iterate through the ResultSet and add rows to the DefaultTableModel
            while (rs.next()) {
                Object[] rowData = new Object[5];
                rowData[0] = rs.getInt("coffee_id");
                rowData[1] = rs.getString("coffee_name");
                rowData[2] = rs.getString("coffee_description");
                rowData[3] = rs.getDouble("price");
                rowData[4] = rs.getInt("num_in_stock");
                dtm.addRow(rowData);
            }

            // Set the DefaultTableModel as the model for jTable1
            jTable2.setModel(dtm);

            // Close the ResultSet, Statement, and Connection
            rs.close();
            stmt.close();
            conn.close();
        } catch (ClassNotFoundException | SQLException ex) {
            ex.printStackTrace();
        }
    }

